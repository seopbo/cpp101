#include <iostream>

int main() {
    using namespace std;

    int watermelons = 3;

    cout << "watermelons: " << watermelons << endl;
    cout << "watermelons++ " << watermelons++ << endl;
    cout << "watermelons: " << watermelons << endl;

    int carrots = 4;

    cout << "carrots: " << carrots << endl;
    cout << "++carrots " << ++carrots << endl;
    cout << "carrots: " << carrots << endl;
    return 0;
}
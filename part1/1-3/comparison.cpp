#include <iostream>

int main() {
    using namespace std;

    int numOfWatermelons = 3;
    cout << "watermelons: " << numOfWatermelons << endl;
    cout << "numOfWatermelons >= 2: " << (numOfWatermelons >= 2) << endl;
    cout << "numOfWatermelons < 2: " << (numOfWatermelons < 2) << endl;
    cout << "numOfWatermelons < 2: " << (numOfWatermelons == 2) << endl;
    return 0;
}